public class MathOperations {

    // Method for add integer
    public int add(int a, int b) {
        return a + b;
    }

    // Overloading: Method for add decimal
    public double add(double a, double b) {
        return a + b;
    }

    // Overloading: Method for add 3 integer
    public int add(int a, int b, int c) {
        return a + b + c;
    }
}
